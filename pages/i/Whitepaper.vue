      <p class="subheading understanding center space margin">IDEAS, PROJECTS, ECOSYSTEMS</p>
      <p class="base">The money is allocated to the different ideas coming from the participants through their voting. Every second the microeuros are pre-allocated proportionally to the ideas: the more the votes, the more the proportional amount of money. The collected funds are pre-allocated until the ideas budget is reached. When it happens, the idea turns into a "project" starting the budget release phase. After the project phase is completed, the idea becomes an "ecosystem". Ecosystem's profits go back to the collection fund.</p>

      <p class="subheading understanding center space margin">BUDGET 0 IDEAS</p>
      <p class="base">Ideas may need no budget. For example, they can be a tool for a decision making supported by voting. Some of them, instead, like courses, concerts, dinners, parties.. may not require a budget but a  minimum number of participants that commit themselves to the payment of a ticket or a fee, without which the ideas are not sustainable. All these ideas do not participate to the collection fund distribution, but their possible profit goes to the collection fund anyways, as all activities are co-owned by Cooperacy.</p>

      <p class="subheading understanding center space margin">AFTF MULTIDIMENSIONAL VOTING</p>
      <p class="base">In Cooperacy we don't vote for people, but for ideas. Both ideas and voting are anonymous until the ideas become projects. After that, all the information will be transparent for checking purposes. This is called AFTF, Anonymous First, Transparent Forever-after: the beginning anonymity allows to boost collective intelligence and transparency enhances cooperation afterwards. <br> Anybody can vote for as many ideas they want. In order to leverage more collective intelligence, there are different kinds of votes:
      </p><br>

        <p class="base equivalence">Equivalence</p>
        <p class="base">It determines the proportional allocation of funds. Technical: the proportions are calculated with the number of the equivalence votes the idea has, divided by the total of all the equivalence votes all the ideas have (Ei / Et ). Equivalence-voters become participants of the future projects.
        </p><br>

        <p class="base trust">Trust</p>
        <p class="base">Trust votes make the project skip the first budget step. Technical: if trust votes are more than 75% of equivalence votes (Ti > 75% Ei) the first budget step is directly approved. Trust votes in a "budget 0 idea" indicate participation as a Trustee, that is, as a client, affiliate, pre-buy, subscription to a course or pre-purchase to a concert. This vinculates the trustee to the payment of the relative pre-purchase or fee after the first milestone is passed.</p><br>

        <p class="base care">Care</p>
        <p class="base">It’s for sustainability projects or ones that generate emotional response in the voter. The emotional response in the future could also be generated thanks to biofeedback sensors. When care-voted, the project gains higher visibility. Technical: Ideas visibility is enhanced by Care votes percentages (Ci / Ct )</p><br>

        <p class="base transparency">Transparency</p>
        <p class="base">This vote determines wether the idea is hiding some information or cheating or spamming. Technical: if the number of transparency votes, subtracted of half of the trust votes, are more than 25% of Equivalence votes (Ii - 50% Ti > 25% Ei ) the idea or project is blocked until the values are back to normal ( Ii - 50% Ti &lt; 25% Ei ) After 90 days of blockage, the idea is removed and the budget is turned back to the collection fund. During this period the participants can better explain to voters the idea development in order to provoke a change in the number of transparency and trust votes. If the idea is unblocked, the count of 90 days is canceled. Transparency voters are never anonymous. Highly transparency-voted ideas go to a final year list in which the most transparency-voted idea wins the prize of joke/trolling idea of the year. (When clicking, insert an alert that explains the functioning). </p><br>

        <p class="base freedom">Freedom</p>
